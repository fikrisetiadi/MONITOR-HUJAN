    <h1>Monitoring Deteksi Hujan</h1>
</div>
<center>
<br>

<h3>
<p>Sistem monitoring deteksi hujan berbasis maps dan IoT ini adalah sebuah website yang dapat memonitoring deteksi hujan disuatu tempat. Pada website ini akan menampilkan data hujan yang berupa data analog kemudian juga menampilkan hasil dari data tersebut apakah sedang hujan ringan, hujan sedang atau pun hujan lebat.</p>
</h3>
<h3>
<p>Pada website ini juga akan menampilkan tampilan maps lokasi yang menjadi objek di tempatkan alat deteksi hujan tersebut yang terintegrasi dengan data hujan.Untuk alat pendeteksi hujan secara realtime menggunakan mikrocontroller ESP8266 dengan sensor hujan. Alat ini akan dihubungkan melalui Wifi kedalam database website monitoring.</p>
</h3>
